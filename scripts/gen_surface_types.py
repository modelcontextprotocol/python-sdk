"""Regenerate the per-version wire-shape surface packages from vendored schemas.

The two surfaces under ``src/mcp/types/v<version>/__init__.py`` are pure
generator output: this script is the only writer. For each entry of
``schema/PINNED.json`` it verifies the vendored ``schema/<version>.json``
against the recorded sha256, runs ``datamodel-code-generator`` (installed via
the ``codegen`` dependency group), applies the post-processing passes
documented on each pass function below, runs ruff, imports the candidate to
audit definition names / field requiredness / ``_meta`` openness, and writes
the result.

Usage (from the repo root)::

    uv run --group codegen python scripts/gen_surface_types.py [--check]

``--check`` regenerates to a temp location and diffs against the committed
files; non-zero exit (with a unified diff) on any drift.
"""

