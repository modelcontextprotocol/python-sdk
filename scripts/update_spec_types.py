"""Regenerate the spec-version model modules (test oracles and src packages).

Two output flavors share one fetch -> generate -> post-process core:

``--oracle`` (default) regenerates the per-spec-version oracle modules under
tests/spec_oracles/. Pipeline, per selected entry of tests/spec_oracles/PINNED.json:

1. Fetch ``schema.json`` (and ``schema.ts`` for the protocol-version string) at
   the pinned commit SHA, via ``gh api`` — or read both from ``--schema-dir``.
2. Generate pydantic models with the pinned datamodel-code-generator version,
   invoked through ``uvx`` (no project dependency).
3. Post-process with stdlib ast/re: ``RootModel`` wrappers become type aliases
   (recursive groups become ``TypeAliasType``), the synthetic ``Model`` root
   artifact and stale ``model_rebuild()`` calls are dropped, ``{@link X}``
   JSDoc artifacts are unwrapped, and the per-version rename map is applied.
4. Prepend a provenance header and append the ``SPEC_DEFS`` manifest.
5. ``ruff check --fix`` + ``ruff format`` on a staging file inside
   tests/spec_oracles/ (so the repo config and first-party import
   classification apply); any residual finding other than E501 (per-file-
   ignored for generated modules) aborts the run.
6. Verify: import the staged candidate, audit that every schema definition
   resolves to a module attribute and that field wire names and requiredness
   match the schema. Only then rename into ``tests/spec_oracles/<module>.py``
   and rewrite the derived PINNED.json fields.

``--src`` scaffolds the committed per-version wire-model packages under
src/mcp/types/v<version>/. Same core, then the source passes documented on
each pass function below (value-type downgrades, per-class extra policy,
``_meta``-carrier rebasing, inheritance flattening, deterministic synthetic
names, early content arms, docstring wrapping, provenance docstring), a strict
ruff stage (no residual findings at all), and the import-time audits. It is a
scaffold, not a sync mechanism: it refuses to overwrite an existing package
without ``--force``, and the emitted packages are maintained as ordinary
source afterwards.

Usage (from the repo root)::

    uv run --frozen python scripts/update_spec_types.py [KEY ...] [options]

    KEY            entries to (re)generate: 2024-11-05, 2025-03-26, 2025-06-18,
                   2025-11-25, draft. Default: all non-frozen entries.
    --all          select every entry.
    --src          emit src/mcp/types/v<version>/__init__.py packages instead
                   of test oracles.
    --sha SHA      override the pinned SHA for the (single) selected entry and
                   write it back to PINNED.json on success (oracle mode only).
    --schema-dir D offline mode: read schemas from D instead of fetching.
                   Naming: schema-<key>.json / schema-<key>.ts. The caller
                   asserts the files correspond to the pinned SHAs.
    --check        regenerate and diff against the committed modules; exit 1 on
                   drift, change nothing.
    --force        allow regenerating frozen (released-version) oracle entries,
                   or overwriting an existing src package.
"""

