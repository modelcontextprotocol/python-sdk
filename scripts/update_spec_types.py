def downgrade_value_transforms(source: str) -> str:
    """Replace value-transforming pydantic types with plain ``str``.

    URL normalization and base64 re-encoding change bytes on a validate ->
    re-dump round trip; the wire models must reproduce input bytes exactly.
    """
    source = re.sub(r"\b(AnyUrl|FileUrl|Base64Str)\b", "str", source)
    # Drop the now-unused names from the pydantic import (ruff would flag them).
    match = re.search(r"^from pydantic import (.+)$", source, flags=re.MULTILINE)
    if match:
        names = [n.strip() for n in match.group(1).split(",")]
        kept = [n for n in names if n != "str"]
        source = source.replace(match.group(0), f"from pydantic import {', '.join(kept)}")
    return source


def widen_structured_content(source: str) -> str:
    """Widen typed ``structuredContent`` fields to ``Any``.

    The newest schema types the field ``Any``; older schemas say
    ``dict[str, Any]``. The wire models never narrow values an SDK model
    accepts, so a scalar structured result passes through at every version.
    """
    tree = ast.parse(source)
    lines = source.splitlines(keepends=True)
    statements = [
        stmt
        for node in tree.body
        if isinstance(node, ast.ClassDef)
        for stmt in node.body
        if isinstance(stmt, ast.AnnAssign)
        and isinstance(stmt.target, ast.Name)
        and stmt.target.id == "structured_content"
    ]
    for stmt in sorted(statements, key=lambda s: s.lineno, reverse=True):
        start, end = stmt.lineno, _end_lineno(stmt)
        segment = "".join(lines[start - 1 : end])
        lines[start - 1 : end] = [segment.replace("dict[str, Any]", "Any")]
    return "".join(lines)


def _class_field_aliases(node: ast.ClassDef) -> list[tuple[ast.AnnAssign, str, str]]:
    """(statement, field name, wire alias) for every annotated field of a class."""
    out: list[tuple[ast.AnnAssign, str, str]] = []
    for stmt in node.body:
        if not isinstance(stmt, ast.AnnAssign) or not isinstance(stmt.target, ast.Name):
            continue
        annotation_text = ast.unparse(stmt.annotation)
        alias_match = _FIELD_ALIAS_RE.search(annotation_text)
        alias = alias_match.group(1) if alias_match else stmt.target.id
        out.append((stmt, stmt.target.id, alias))
    return out


def flatten_inheritance(source: str) -> tuple[str, list[str]]:
    """Flatten classes that subclass other generated classes.

    The generator emits subclassing when one definition narrows another (the
    named-error family, the 2025-11-25 task results); pyright's
    dataclass-transform rules reject the narrowed overrides, and the committed
    packages carry no suppressions. Flattening re-declares the inherited
    fields directly: base fields in base order, overrides in place, own fields
    appended.
    """
    tree = ast.parse(source)
    lines = source.splitlines(keepends=True)
    classes: dict[str, ast.ClassDef] = {n.name: n for n in tree.body if isinstance(n, ast.ClassDef)}

    def field_segments(node: ast.ClassDef) -> list[tuple[str, str]]:
        """(field name, source text incl. trailing docstring) per field."""
        segments: list[tuple[str, str]] = []
        body = list(node.body)
        for index, stmt in enumerate(body):
            if not isinstance(stmt, ast.AnnAssign) or not isinstance(stmt.target, ast.Name):
                continue
            end = _end_lineno(stmt)
            if index + 1 < len(body):
                nxt = body[index + 1]
                if isinstance(nxt, ast.Expr) and isinstance(nxt.value, ast.Constant):
                    if isinstance(nxt.value.value, str):
                        end = _end_lineno(nxt)
            segments.append((stmt.target.id, "".join(lines[stmt.lineno - 1 : end])))
        return segments

    resolved: dict[str, list[tuple[str, str]]] = {}

    def resolve(name: str) -> list[tuple[str, str]]:
        if name in resolved:
            return resolved[name]
        node = classes[name]
        base_names = [b.id for b in node.bases if isinstance(b, ast.Name) and b.id in classes]
        merged: list[tuple[str, str]] = []
        for base in base_names:
            for field_name, text in resolve(base):
                if field_name not in {n for n, _ in merged}:
                    merged.append((field_name, text))
        for field_name, text in field_segments(node):
            existing = [i for i, (n, _) in enumerate(merged) if n == field_name]
            if existing:
                merged[existing[0]] = (field_name, text)
            else:
                merged.append((field_name, text))
        resolved[name] = merged
        return merged

    flattened: list[str] = []
    replacements: list[tuple[int, int, str]] = []
    for name, node in classes.items():
        generated_bases = [b.id for b in node.bases if isinstance(b, ast.Name) and b.id in classes]
        if not generated_bases:
            continue
        flattened.append(name)
        parts: list[str] = [f"class {name}(WireModel):\n"]
        if ast.get_docstring(node) is not None:
            stmt = node.body[0]
            parts.append("".join(lines[stmt.lineno - 1 : _end_lineno(stmt)]))
        parts.extend(
            "".join(lines[stmt.lineno - 1 : _end_lineno(stmt)])
            for stmt in node.body
            if isinstance(stmt, ast.Assign)
            and any(isinstance(t, ast.Name) and t.id == "model_config" for t in stmt.targets)
        )
        parts.extend(text for _, text in resolve(name))
        replacements.append((node.lineno, _end_lineno(node), "".join(parts)))

    for start, end, new in sorted(replacements, reverse=True):
        lines[start - 1 : end] = [new]
    return "".join(lines), sorted(flattened)


def rebase_meta_carriers(source: str) -> tuple[str, list[str]]:
    """Rebase classes referenced from ``_meta`` fields onto ``OpenWireModel``.

    Unknown ``_meta`` keys must survive a validate -> re-dump round trip at
    every version, so their carrier classes keep ``extra="allow"`` through the
    open base rather than a per-class config.
    """
    tree = ast.parse(source)
    classes: dict[str, ast.ClassDef] = {n.name: n for n in tree.body if isinstance(n, ast.ClassDef)}
    carriers: set[str] = set()
    for node in classes.values():
        for stmt, _, alias in _class_field_aliases(node):
            if alias != "_meta":
                continue
            annotation_text = ast.unparse(stmt.annotation)
            carriers.update(name for name in classes if re.search(rf"\b{name}\b", annotation_text))
    for name in sorted(carriers):
        source = re.sub(
            rf"^class {name}\(WireModel\):$",
            f"class {name}(OpenWireModel):",
            source,
            flags=re.MULTILINE,
        )
    return source, sorted(carriers)


def apply_extra_policy(source: str, carriers: list[str]) -> tuple[str, list[str]]:
    """Force ``extra="ignore"`` everywhere except the enumerated open classes.

    The generator emits per-class ``extra="allow"`` wherever the schema has an
    index signature. Open models would retain fields the target version never
    defined, so a version-mismatched field would never register as a loss on
    revalidation; closing them is what makes those fields detectable. The
    classes that stay open are exactly: ``_meta`` carriers (via the open
    base), the tool input/output schema interiors, and the subscription
    filter.
    """
    tree = ast.parse(source)
    lines = source.splitlines(keepends=True)
    classes: dict[str, ast.ClassDef] = {n.name: n for n in tree.body if isinstance(n, ast.ClassDef)}

    keep_allow: set[str] = set()
    for node in classes.values():
        for stmt, _, alias in _class_field_aliases(node):
            if alias in _OPEN_INTERIOR_FIELD_ALIASES:
                annotation_text = ast.unparse(stmt.annotation)
                keep_allow.update(name for name in classes if re.search(rf"\b{name}\b", annotation_text))
    if "SubscriptionFilter" in classes:
        keep_allow.add("SubscriptionFilter")

    replacements: list[tuple[int, int, str]] = []
    for name, node in classes.items():
        config_stmt = next(
            (
                stmt
                for stmt in node.body
                if isinstance(stmt, ast.Assign)
                and any(isinstance(t, ast.Name) and t.id == "model_config" for t in stmt.targets)
            ),
            None,
        )
        if config_stmt is None:
            continue
        value = config_stmt.value
        is_plain_allow = (
            isinstance(value, ast.Call)
            and isinstance(value.func, ast.Name)
            and value.func.id == "ConfigDict"
            and len(value.keywords) == 1
            and value.keywords[0].arg == "extra"
            and isinstance(value.keywords[0].value, ast.Constant)
            and value.keywords[0].value.value == "allow"
        )
        if not is_plain_allow:
            raise PipelineError(f"unexpected model_config on {name}: {ast.unparse(config_stmt)}")
        if name in keep_allow and name not in carriers:
            comment = _SUBSCRIPTION_FILTER_COMMENT if name == "SubscriptionFilter" else _OPEN_INTERIOR_COMMENT
            replacements.append((config_stmt.lineno, config_stmt.lineno - 1, comment))
            continue
        remaining = [stmt for stmt in node.body if stmt is not config_stmt]
        new = "" if remaining else "    pass\n"
        replacements.append((config_stmt.lineno, _end_lineno(config_stmt), new))

    for start, end, new in sorted(replacements, reverse=True):
        lines[start - 1 : end] = [new]
    return "".join(lines), sorted(keep_allow)


def deterministic_synthetic_names(source: str) -> tuple[str, dict[str, str], list[str]]:
    """Rename counter-based synthetic classes after their single owner.

    The generator names inline params/meta objects ``Params``/``Params1``/...
    in discovery order, so an inserted definition renumbers every later one
    and pollutes cross-version diffs. A synthetic class referenced by exactly
    one field of one owner is renamed ``<Owner>Params``/``<Owner>Meta``;
    shared or colliding names keep their generated form.
    """
    tree = ast.parse(source)
    classes: dict[str, ast.ClassDef] = {n.name: n for n in tree.body if isinstance(n, ast.ClassDef)}
    synthetic = {name for name in classes if re.fullmatch(r"(?:Params|Meta)\d*", name)}
    references: dict[str, list[tuple[str, str]]] = {name: [] for name in synthetic}
    for node in classes.values():
        for stmt, field_name, _ in _class_field_aliases(node):
            annotation_text = ast.unparse(stmt.annotation)
            for name in synthetic:
                if re.search(rf"\b{name}\b", annotation_text):
                    references[name].append((node.name, field_name))

    renames: dict[str, str] = {}
    skipped: list[str] = []
    taken = set(classes)
    for name in sorted(synthetic):
        refs = references[name]
        if len(refs) != 1:
            skipped.append(f"{name} ({len(refs)} references)")
            continue
        owner, field_name = refs[0]
        suffix = {"params": "Params", "meta": "Meta"}.get(field_name)
        if suffix is None:
            skipped.append(f"{name} (referenced from field {field_name!r})")
            continue
        target = f"{owner}{suffix}"
        if target in taken:
            skipped.append(f"{name} (target {target} already exists)")
            continue
        renames[name] = target
        taken.add(target)

    if renames:
        pattern = re.compile(r"\b(" + "|".join(sorted(renames, key=len, reverse=True)) + r")\b")
        source = pattern.sub(lambda m: renames[m.group(1)], source)
    return source, renames, skipped


def add_early_content_arms(source: str, protocol_version: str) -> tuple[str, list[str]]:
    """Carry newer content-block definitions into the older packages.

    ``audio`` (2025-03-26) and ``resource_link`` (2025-06-18) content emitted
    to an older peer passes through unchanged rather than being refused, so
    the older packages carry the definitions and union arms early.
    """
    added: list[str] = []
    if protocol_version == "2024-11-05":
        source = _splice_class_after(
            source,
            "ImageContent",
            _T5_COMMENT.format(intro_version="2025-03-26") + _AUDIO_CONTENT_TEMPLATE,
        )
        source = _add_union_arm(source, "TextContent | ImageContent", "TextContent | ImageContent | AudioContent")
        added.append("AudioContent")
    if protocol_version in ("2024-11-05", "2025-03-26"):
        source = _splice_class_after(
            source,
            "EmbeddedResource",
            _T5_COMMENT.format(intro_version="2025-06-18") + _RESOURCE_LINK_TEMPLATE,
        )
        source = _add_union_arm(source, "| EmbeddedResource", "| ResourceLink | EmbeddedResource")
        added.append("ResourceLink")
    return source, added


def _splice_class_after(source: str, anchor_class: str, class_text: str) -> str:
    """Insert a class definition immediately after ``anchor_class``."""
    tree = ast.parse(source)
    anchor = next((n for n in tree.body if isinstance(n, ast.ClassDef) and n.name == anchor_class), None)
    if anchor is None:
        raise PipelineError(f"cannot splice after {anchor_class!r}: class not found")
    lines = source.splitlines(keepends=True)
    end = _end_lineno(anchor)
    lines[end:end] = ["\n\n" + class_text]
    return "".join(lines)


def _add_union_arm(source: str, old: str, new: str) -> str:
    """Widen every content union matching ``old`` and comment the change."""
    note = "    # Carries arms beyond this version's schema; see the class comments above.\n"
    out_lines: list[str] = []
    for line in source.splitlines(keepends=True):
        if old in line and not line.lstrip().startswith(("#", "class ")):
            if not out_lines or out_lines[-1] != note:
                out_lines.append(note)
            line = line.replace(old, new)
        out_lines.append(line)
    return "".join(out_lines)


def _join_class_docstring_summaries(source: str) -> str:
    """Move each class docstring summary onto the opening quote line.

    This is the fix the D212 lint rule would apply later; doing it first
    keeps the long-line wrapping below in charge of the resulting line
    length. Field annotation strings are not docstrings and stay untouched.
    """
    tree = ast.parse(source)
    lines = source.splitlines(keepends=True)
    for node in tree.body:
        if not isinstance(node, ast.ClassDef) or ast.get_docstring(node) is None:
            continue
        stmt = node.body[0]
        first = lines[stmt.lineno - 1]
        if first.strip() != '"""' or _end_lineno(stmt) == stmt.lineno:
            continue
        summary = lines[stmt.lineno].strip()
        single_content = _end_lineno(stmt) == stmt.lineno + 2
        indent = first[: len(first) - len(first.lstrip())]
        if single_content:
            # The formatter collapses a one-line docstring body onto a single
            # line; emit that form directly when it fits, otherwise pre-wrap
            # the summary so the body stays multi-line.
            if len(indent) + len(summary) + 6 <= 120:
                lines[stmt.lineno - 1] = f'{indent}"""{summary}"""\n'
                lines[stmt.lineno] = ""
                lines[stmt.lineno + 1] = ""
            else:
                # Width leaves room for the closing quotes in case the wrap
                # still fits on one line and the formatter collapses it.
                wrapped = textwrap.wrap(
                    summary,
                    width=117,
                    initial_indent=indent + '"""',
                    subsequent_indent=indent,
                    break_long_words=False,
                    break_on_hyphens=False,
                )
                lines[stmt.lineno - 1] = "".join(part + "\n" for part in wrapped)
                lines[stmt.lineno] = ""
            continue
        lines[stmt.lineno - 1] = first.rstrip("\n") + summary + "\n"
        lines[stmt.lineno] = ""
    return "".join(lines)


def wrap_docstrings(source: str) -> str:
    """Wrap docstring lines to the repo line length.

    The schema prose stays (the file should read as the spec), but committed
    src modules carry no per-file lint exemptions, so long lines wrap. Both
    body lines and opening-quote summary lines are wrapped; anything this
    pass cannot handle fails the strict ruff stage.
    """
    source = _join_class_docstring_summaries(source)
    out: list[str] = []
    in_docstring = False
    for line in source.splitlines(keepends=True):
        quotes = line.count('"""')
        long = len(line.rstrip("\n")) > 120
        indent = line[: len(line) - len(line.lstrip())]
        if not in_docstring:
            if quotes == 1:
                in_docstring = True
                if long:
                    out.extend(
                        part + "\n"
                        for part in textwrap.wrap(
                            line.strip().removeprefix('"""'),
                            width=120,
                            initial_indent=indent + '"""',
                            subsequent_indent=indent,
                            break_long_words=False,
                            break_on_hyphens=False,
                        )
                    )
                    continue
            out.append(line)
            continue
        if quotes:
            in_docstring = False
            out.append(line)
            continue
        if not long:
            out.append(line)
            continue
        out.extend(
            part + "\n"
            for part in textwrap.wrap(
                line.strip(),
                width=120,
                initial_indent=indent,
                subsequent_indent=indent,
                break_long_words=False,
                break_on_hyphens=False,
            )
        )
    return "".join(out)


def assert_only_wire_bases(source: str) -> None:
    """Assert every class in the emitted package sits on one of the two bases."""
    tree = ast.parse(source)
    for node in tree.body:
        if not isinstance(node, ast.ClassDef):
            continue
        base_names = [ast.unparse(b) for b in node.bases]
        if base_names not in (["WireModel"], ["OpenWireModel"]):
            raise PipelineError(f"class {node.name} has unexpected bases {base_names} after the src passes")


def build_src_candidate(entry: Entry, sha: str, schema_dir: Path | None, generator_version: str) -> tuple[str, Outcome]:
    """src-mode pipeline for one entry; returns (candidate source, outcome)."""
    if importlib.util.find_spec("mcp.types._wire_base") is None:
        raise PipelineError("src mode requires src/mcp/types/_wire_base.py (WireModel/OpenWireModel) to exist")
    schema_json_text, ts_text = obtain_schemas(entry, sha, schema_dir)
    schema: dict[str, Any] = json.loads(schema_json_text)
    protocol_version = extract_protocol_version(entry, ts_text)
    entry.protocol_version = protocol_version
    entry.module = module_name_for(protocol_version)
    entry.sha = sha
    rename_map = RENAME_MAP[entry.key]

    with tempfile.TemporaryDirectory() as tmp:
        tmp_dir = Path(tmp)
        schema_file = tmp_dir / "schema.json"
        schema_file.write_text(schema_json_text)
        raw_file = tmp_dir / "raw.py"
        warnings = run_generator(schema_file, raw_file, generator_version, _SRC_BASE_CLASS)
        processed = postprocess(raw_file.read_text(), rename_map)
    assert_alias_blocks_present(processed.source, processed.recursive)

    notes: list[str] = []
    source = downgrade_value_transforms(processed.source)
    source = widen_structured_content(source)
    source, flattened = flatten_inheritance(source)
    if flattened:
        notes.append(f"flattened: {', '.join(flattened)}")
    source, carriers = rebase_meta_carriers(source)
    if carriers:
        notes.append(f"meta carriers on OpenWireModel: {', '.join(carriers)}")
        source = source.replace(
            "from mcp.types._wire_base import WireModel",
            "from mcp.types._wire_base import OpenWireModel, WireModel",
        )
    source, keep_allow = apply_extra_policy(source, carriers)
    if keep_allow:
        notes.append(f"extra=allow kept: {', '.join(sorted(set(keep_allow) - set(carriers)))}")
    source, renames, skipped = deterministic_synthetic_names(source)
    notes.append(f"synthetic renames: {len(renames)}, kept generated names: {len(skipped)}")
    if skipped:
        notes.append(f"kept: {'; '.join(skipped)}")
    source, t5 = add_early_content_arms(source, protocol_version)
    if t5:
        notes.append(f"early content arms: {', '.join(t5)}")
    source = wrap_docstrings(source)

    header = SRC_HEADER_TEMPLATE.format(
        protocol_version=protocol_version,
        schema_path=entry.schema_path,
        sha=sha,
        generator_version=generator_version,
        version_notes=_DRAFT_INTERIOR_NOTE if protocol_version == "2026-07-28" else "",
    )
    staging = SRC_TYPES_DIR / f"_staging_{entry.module}.py"
    try:
        staging.write_text(header + source)
        run_ruff(staging, allow_e501=False)
        candidate = staging.read_text()
        assert_only_wire_bases(candidate)
        mod = import_candidate(staging, entry.module)
        audit_candidate(mod, schema, entry, rename_map)
        audit_meta_carriers(mod, entry)
    finally:
        staging.unlink(missing_ok=True)

    outcome = Outcome(
        entry=entry,
        def_count=len(schema_defs(schema)),
        converted_aliases=processed.converted,
        recursive_aliases=processed.recursive,
        generator_warnings=warnings,
        e501_count=0,
        src_notes=notes,
    )
    return candidate, outcome


def write_back_pinned(pinned: dict[str, Any], entries: list[Entry]) -> None:
    """Rewrite the derived fields (sha, protocol_version, module) into PINNED.json."""
    by_key = {e.key: e for e in entries}
    for table in (pinned["versions"], pinned["extensions"]):
        for key, data in table.items():
            if key in by_key:
                entry = by_key[key]
                data["sha"] = entry.sha
                data["protocol_version"] = entry.protocol_version
                data["module"] = entry.module
    PINNED_PATH.write_text(json.dumps(pinned, indent=2) + "\n")


def delete_orphans(pinned: dict[str, Any]) -> list[str]:
    """Delete generated oracle modules that no PINNED.json entry produces any more."""
    known = {data["module"] for data in pinned["versions"].values()}
    known |= {data["module"] for data in pinned["extensions"].values()}
    deleted: list[str] = []
    for path in sorted(ORACLES_DIR.glob("*.py")):
        if path.stem.startswith(("_", "test_")) or path.name == "__init__.py":
            continue
        if path.stem not in known:
            path.unlink()
            deleted.append(path.name)
    return deleted


def summarize(outcomes: list[Outcome], *, src: bool) -> None:
    """Print the per-entry generation summary."""
    for o in outcomes:
        target = f"src/mcp/types/{o.entry.module}/__init__.py" if src else f"tests/spec_oracles/{o.entry.module}.py"
        print(
            f"{o.entry.key}: {o.action} -> {target} "
            f"(defs={o.def_count}, alias_conversions={o.converted_aliases}, "
            f"recursive={o.recursive_aliases or 'none'}, residual_E501={o.e501_count})"
        )
        for note in o.src_notes:
            print(f"  {note}")
        for warning in o.generator_warnings:
            print(f"  generator warning: {warning}")


def main(argv: list[str] | None = None) -> int:
    """CLI entry point."""
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("keys", nargs="*", help="PINNED.json entries to regenerate (default: all non-frozen)")
    parser.add_argument("--all", action="store_true", help="select every entry")
    parser.add_argument("--src", action="store_true", help="emit src/mcp/types/v<version>/ packages")
    parser.add_argument("--sha", help="override the pinned SHA (single KEY only); written back on success")
    parser.add_argument("--schema-dir", type=Path, help="read schemas from this directory instead of fetching")
    parser.add_argument("--check", action="store_true", help="diff regenerated output against committed modules")
    parser.add_argument("--force", action="store_true", help="regenerate frozen entries / overwrite src packages")
    args = parser.parse_args(argv)

    if str(REPO_ROOT) not in sys.path:
        sys.path.insert(0, str(REPO_ROOT))

    pinned = load_pinned()
    entries = iter_entries(pinned)
    valid_keys = [e.key for e in entries]
    if args.all:
        selected = entries
    elif args.keys:
        unknown = [k for k in args.keys if k not in valid_keys]
        if unknown:
            parser.error(f"unknown KEY(s) {unknown}; valid: {valid_keys}")
        selected = [e for e in entries if e.key in args.keys]
    elif args.src:
        selected = entries
    else:
        selected = [e for e in entries if not e.frozen]
    if args.sha and (len(selected) != 1 or args.src):
        parser.error("--sha requires exactly one KEY in oracle mode")

    generator_version: str = pinned["generator"]["version"]
    outcomes: list[Outcome] = []
    drifted: list[str] = []
    regenerated: list[Entry] = []
    for entry in selected:
        if args.src:
            candidate, outcome = build_src_candidate(entry, entry.sha, args.schema_dir, generator_version)
            target = SRC_TYPES_DIR / entry.module / "__init__.py"
            if args.check:
                if target.is_file() and target.read_text() == candidate:
                    outcome.action = "checked"
                else:
                    outcome.action = "drifted"
                    drifted.append(entry.module)
            elif target.is_file() and not args.force:
                outcome.action = "exists (scaffold refused; pass --force to overwrite)"
            else:
                target.parent.mkdir(parents=True, exist_ok=True)
                target.write_text(candidate)
                outcome.action = "installed"
                import_check(f"mcp.types.{entry.module}")
            outcomes.append(outcome)
            continue
        target = ORACLES_DIR / f"{entry.module}.py"
        if entry.frozen and target.is_file() and not (args.force or args.check):
            print(f"{entry.key}: frozen (released schemas are generate-once); use --force to regenerate")
            continue
        candidate, outcome = build_candidate(entry, args.sha or entry.sha, args.schema_dir, generator_version)
        target = ORACLES_DIR / f"{entry.module}.py"  # module may have been re-derived
        if args.check:
            if target.is_file() and target.read_text() == candidate:
                outcome.action = "checked"
            else:
                outcome.action = "drifted"
                drifted.append(entry.module)
        else:
            if target.is_file() and target.read_text() == candidate:
                outcome.action = "unchanged"
            else:
                target.write_text(candidate)
                outcome.action = "installed"
            import_check(f"tests.spec_oracles.{entry.module}")
            regenerated.append(entry)
        outcomes.append(outcome)

    if regenerated and not args.check:
        write_back_pinned(pinned, regenerated)
        for name in delete_orphans(pinned):
            print(f"deleted orphaned module: {name}")

    summarize(outcomes, src=args.src)
    if drifted:
        print(f"DRIFT: {drifted} differ from committed output", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
